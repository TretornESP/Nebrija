static int64_t write(device_addr_t id, uint64_t offset, uint64_t size, const uint8_t* buffer) {
    (void)offset;
    struct framebuffer_info* info = &fb_info[id];
    for (uint64_t i = 0; i < size; i++) {
        framebuffer_putchar(info, buffer[i]);
    }
    return SUCCESS;
}
