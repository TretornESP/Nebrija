void framebuffer_putpixel(struct framebuffer_info * info, uint64_t x, uint64_t y, uint32_t col) {
	//TODO same as clearscreen issue in comment.
	info->vmem[x + y * info->width] = col;
}

void framebuffer_clearscreen(struct framebuffer_info * info, uint32_t col) {
	//TODO this code assumes bpp of 32 ARGB, but the bpp and format may be different
	for(uint64_t i = 0; i < info->width * info->height; i++) {
		info->vmem[i] = col;
	}
}

void framebuffer_scroll(struct framebuffer_info * info) {
	uint64_t y0 = info->height - 16 - (info->height % 16);
	uint64_t n = (info->width * info->height - info->width * 16) * 4;
	memmove(info->vmem, info->vmem + info->width * 16, n);
	for(uint64_t y = 0; y < 16 + (info->height % 16); y++) {
		for(uint64_t x = 0; x < info->width; x++) {
			framebuffer_putpixel(info, x, y0 + y, info->bc);
		}
	}
}

void framebuffer_putchar(struct framebuffer_info * info, char ch) {
	
	if(ch == '\n') {
		info->posx = 0;
		if(info->posy >= ((info->height - (info->height % 16)) / 16) - 1) {
			framebuffer_scroll(info);
		} else {
			info->posy++;
		}
		return;
	}
	if(ch == '\t') {
		for(uint64_t i = 0; i < TABSIZE; i++) {
			framebuffer_putchar(info, ' ');
		}
		return;
	}
	if (ch == '\b') {
		if(info->posx == 0) {
			if(info->posy == 0) {
				return;
			}
			info->posx = ((info->width - (info->width % 8)) / 8) - 1;
			info->posy--;
		} else {
			info->posx--;
		}
		framebuffer_putchar(info, ' ');
		if(info->posx == 0) {
			if(info->posy == 0) {
				return;
			}
			info->posx = ((info->width - (info->width % 8)) / 8) - 1;
			info->posy--;
		} else {
			info->posx--;
		}
		return;
	}

	uint64_t x0 = info->posx * 8;
	uint64_t y0 = info->posy * 16;

	for(uint64_t y = 0; y < 16; y++) {
		for(uint64_t x = 0; x < 8; x++) {
			if((font[ch * 16 + y]) >> (7 - x) & 1) {
				framebuffer_putpixel(info, x0 + x, y0 + y, info->fc);
			} else {
				framebuffer_putpixel(info, x0 + x, y0 + y, info->bc);
			}
		}
	}
	
	if(info->posx >= ((info->width - (info->width % 8)) / 8) - 1) {
		info->posx = 0;
		if(info->posy >= ((info->height - (info->height % 16)) / 16) - 1) {
			framebuffer_scroll(info);
		} else {
			info->posy++;
		}
	} else {
		info->posx++;
	}
}

